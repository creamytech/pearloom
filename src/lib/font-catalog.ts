// ── Build a Google Fonts URL for a pair ─────────────────────
export function buildFontsUrl(pair: Pick<FontPair, 'heading' | 'body' | 'headingWeight' | 'bodyWeight' | 'headingStyle'>): string {
  const encode = (name: string) => name.replace(/ /g, '+');
  const hStyle = pair.headingStyle === 'italic' ? `ital,wght@1,${pair.headingWeight}` : `wght@${pair.headingWeight}`;
  const bStyle = `wght@${pair.bodyWeight}`;
  return `https://fonts.googleapis.com/css2?family=${encode(pair.heading)}:${hStyle}&family=${encode(pair.body)}:${bStyle}&display=swap`;
}
