// ── Helper: get pairs by category ───────────────────────────
export function getPairsByCategory(
  category: FontPair['category'],
): FontPair[] {
  return FONT_CATALOG.filter((p) => p.category === category);
}

// ── Helper: get pair by id ───────────────────────────────────
export function getPairById(id: string): FontPair | undefined {
  return FONT_CATALOG.find((p) => p.id === id);
}

