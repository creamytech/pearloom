/** Hand-placed ornaments that give each template its signature feel.
 *  These ship with the manifest so the rendered site carries the
 *  template's voice into hero, dividers, RSVP card, etc. */
export interface TemplateMotifs {
  /** Large atmospheric blob behind the hero. */
  blob?: 'sage' | 'peach' | 'lavender' | 'cream' | 'dusk' | 'warm' | 'none';
  /** A single wax-seal / ink stamp motif on the hero. */
  stamp?: {
    text: string;
    icon?: 'heart' | 'pear' | 'leaf' | 'ring' | 'sparkles' | 'none';
    tone?: 'peach' | 'sage' | 'lavender' | 'cream' | 'gold';
    rotation?: number;
  };
  /** Hand-drawn squiggle variant. 1 = curl, 2 = wave, 3 = scallop. */
  squiggle?: 1 | 2 | 3;
  /** Sparkle dot accent near the hero. */
  sparkle?: boolean;
  /** Heart accent near the names. */
  heart?: boolean;
  /** Sticky note tucked somewhere on the hero. */
  postIt?: {
    text: string;
    tone?: 'peach' | 'sage' | 'lavender' | 'cream';
    rotation?: number;
  };
  /** Polaroid-style photo frame decoration. */
  polaroid?: boolean;
}

export interface SiteTemplate {
  id: string;
  name: string;
  tagline: string;
  description: string;
  /** Visual preview color (gradient for thumbnail) */
  previewGradient: string;
  /** Occasion types this template works for */
  occasions: string[];
  /** Tags for search */
  tags: string[];
  /** Popularity rank */
  popularity: number;
  /** The actual blocks and manifest overrides */
  blocks: PageBlock[];
  /** Optional explicit block order override (by block type); when
   *  set, the renderer uses this instead of the declared `blocks`
   *  order, letting two templates with the same block set feel
   *  structurally different. */
  blockOrder?: string[];
  /** Blocks hidden for this template (registry for minimalist, etc.) */
  hiddenBlocks?: string[];
  /** Theme overrides */
  theme: {
    colors: {
      background: string;
      foreground: string;
      accent: string;
      accentLight: string;
      muted: string;
      cardBg: string;
    };
    fonts: {
      heading: string;
      body: string;
    };
  };
  /** Default vibe string */
  vibeString: string;
  /** Price in cents (0 = free, undefined = free) */
  price?: number;
  /** Featured in marketplace */
  featured?: boolean;
  /** Hero cover photo URL */
  coverPhoto?: string;
  /** Layout format */
  layoutFormat: string;
  /** Poetry defaults */
  poetry: {
    heroTagline: string;
    closingLine: string;
    rsvpIntro: string;
    welcomeStatement: string;
  };
  /** Signature hand-placed ornaments that ship with the template. */
  motifs?: TemplateMotifs;
  /** Per-template SIGNATURE illustrated decor (replaces the abandoned
   *  motif.stamp). Pulls from TemplateSignatureDecor — values like
   *  'monolith' (Marfa), 'citrus' (Lake Como), 'brushstroke' (Tokyo).
   *  The renderer floats this in the hero corner; gives each template
   *  a real visual identity, not just a typography difference. */
  signatureDecor?:
    // Original 13 (place-specific aesthetics)
    | 'monolith' | 'citrus' | 'champagne-coupe' | 'cliff-fog' | 'candlestick'
    | 'lavender-bundle' | 'brushstroke' | 'tea-bowl' | 'cedar-cone'
    | 'chianti-bottle' | 'loire-key' | 'saguaro' | 'edison'
    // Birthday / party
    | 'disco-ball' | 'wine-coupe' | 'circuit-trace' | 'martini-olive'
    | 'venetian-mask' | 'confetti-burst' | 'art-deco-fan' | 'birthday-candle'
    // Florals & nature
    | 'rose-bloom' | 'peony-bloom' | 'magnolia' | 'pampas-grass'
    | 'wildflower-cluster' | 'pressed-daisy' | 'wheat-stalk' | 'pine-bough'
    | 'palm-frond' | 'snowflake' | 'constellation' | 'mushroom-pair'
    | 'cactus-bloom'
    // Architecture & objects
    | 'copper-pipe' | 'marble-column' | 'stained-glass' | 'anchor-rope'
    | 'bamboo-stem' | 'cameo-oval' | 'quill-ink' | 'laurel-wreath'
    | 'castle-turret'
    // Event-OS specific
    | 'suitcase' | 'champagne-tower' | 'place-setting' | 'time-capsule'
    | 'baby-rattle' | 'baby-bottle' | 'mortarboard' | 'retirement-clock'
    | 'candle-vigil' | 'torah-scroll' | 'baptism-shell' | 'tiara'
    | 'house-key-ring' | 'balloon-cluster'
    | 'none';
}

// ── Template Factory ─────────────────────────────────────────

function makeBlock(type: string, order: number, config?: Record<string, unknown>): PageBlock {
  return {
    id: `${type}-tmpl-${order}`,
    type: type as PageBlock['type'],
    order,
    visible: true,
    config,
  };
}

