import type { PageBlock, StoryManifest } from '@/types';
import { getHeroIllustrationDataUrl } from '@/lib/hero-illustrations';

export interface SiteTemplate {
  id: string;
  name: string;
  tagline: string;
  description: string;
  /** Visual preview color (gradient for thumbnail) */
  previewGradient: string;
  /** Occasion types this template works for */
  occasions: string[];
  /** Tags for search */
  tags: string[];
  /** Popularity rank */
  popularity: number;
  /** The actual blocks and manifest overrides */
  blocks: PageBlock[];
  /** Theme overrides */
  theme: {
    colors: {
      background: string;
      foreground: string;
      accent: string;
      accentLight: string;
      muted: string;
      cardBg: string;
    };
    fonts: {
      heading: string;
      body: string;
    };
  };
  /** Default vibe string */
  vibeString: string;
  /** Price in cents (0 = free, undefined = free) */
  price?: number;
  /** Featured in marketplace */
  featured?: boolean;
  /** Hero cover photo URL */
  coverPhoto?: string;
  /** Layout format */
  layoutFormat: string;
  /** Poetry defaults */
  poetry: {
    heroTagline: string;
    closingLine: string;
    rsvpIntro: string;
    welcomeStatement: string;
  };
}

// ── Template Factory ─────────────────────────────────────────

function makeBlock(type: string, order: number, config?: Record<string, unknown>): PageBlock {
  return {
    id: `${type}-tmpl-${order}`,
    type: type as PageBlock['type'],
    order,
    visible: true,
    config,
  };
}

