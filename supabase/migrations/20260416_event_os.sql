  owner_email text NOT NULL,
  budget_cents bigint,
  target_date date,
  target_city text,
  guest_count_estimate int,
  constraints jsonb DEFAULT '{}'::jsonb,
  plan jsonb DEFAULT '{}'::jsonb,     -- full plan object the agent maintains
  checklist jsonb DEFAULT '[]'::jsonb,
  vendor_shortlist jsonb DEFAULT '[]'::jsonb,
  conversation jsonb DEFAULT '[]'::jsonb,   -- full agent message history
  status text DEFAULT 'active',       -- 'active' | 'paused' | 'complete'
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now()
);

