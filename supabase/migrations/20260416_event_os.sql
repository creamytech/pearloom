-- ── Per-guest personalization cache ───────────────────────────
-- Generated lazily on first /i/{token} hit and refreshed when
-- the manifest or guest row changes.

CREATE TABLE IF NOT EXISTS guest_personalization (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  site_id text NOT NULL,
  guest_id uuid UNIQUE REFERENCES pearloom_guests(id) ON DELETE CASCADE,
  hero_copy text,                     -- "Priya — we can't believe it's been 11 years of you picking up the phone at 3am."
  chapter_highlights jsonb,           -- [{chapterId, whyTheyreMentioned}]
  travel_tips jsonb,                  -- {nearestAirport, driveTime, recommendedHotels}
  seat_summary text,                  -- "Table 7 — you're next to your cousin Kian"
  pronunciation_audio_url text,       -- TTS of their name + partner names
  generated_by text DEFAULT 'claude-sonnet-4-6',
  generated_at timestamptz DEFAULT now(),
  expires_at timestamptz
);

